<template>
  <div>
    <div>Please wait...</div>
  </div>
</template>

<script>
import Vue from 'vue'
import { mapActions, mapGetters, mapMutations, mapState } from 'vuex'

export default Vue.extend({
  computed: {
    ...mapState({
      requestState: (state) => state.github.requestState,
    }),
    ...mapGetters({
      stateFromStorage: 'github/getStateFromStorage',
    }),
  },
  async mounted() {
    this.$store.dispatch('door/control', 'opened')
    const code = this.$route.query.code
    const state = this.$route.query.state

    if (state !== this.stateFromStorage) {
      throw new Error('invalid state value')
    }

    await this.fetchClaimInfo(code)
    if (this.$route.query.error) {
      this.$message.error({
        content: `Error: ${this.$route.query.error}`,
        key: 'fetchtch-claim-info',
      })
    } else {
      this.$router.push('/newresult')
    }
  },
  methods: {
    ...mapActions({
      fetchClaimInfo: 'github/fetchClaimInfo',
    }),
    ...mapMutations({
      setCurrentStep: 'claim/setCurrentStep',
    }),
  },
})
</script>
